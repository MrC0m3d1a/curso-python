# Escribir un programa que solicite al usuario una letra y, si es una vocal, 
   # muestre el mensaje “es vocal”.

input('Escriba una letra: ')
letra = input()

if letra == 'a':
        print('es vocal')
elif letra == 'e':
    print('es vocal')
elif letra == 'i':
    print('es vocal')
elif letra == 'o':
    print('es vocal')
elif letra == 'u':
    print('es vocal')

else:
    print('no es vocal')

